    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
     // EDIT LOGIC
        int i = jTable1.getSelectedRow();
        if (i >= 0) {
            Object[] updatedRow = { jTextField6.getText(), jTextField7.getText(), jTextField5.getText(), jComboBox2.getSelectedItem().toString() };
            sellerList.set(i, updatedRow);
            refreshTable();
            JOptionPane.showMessageDialog(this, "Seller Updated Successfully");
        } else {
            JOptionPane.showMessageDialog(this, "Select a row to edit");
        }   
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
    // DELETE LOGIC
        int i = jTable1.getSelectedRow();
        if (i >= 0) {
            sellerList.remove(i);
            refreshTable();
            JOptionPane.showMessageDialog(this, "Seller Deleted");
        } else {
            JOptionPane.showMessageDialog(this, "Select a row to delete");
        }    
    }//GEN-LAST:event_jButton7ActionPerformed

