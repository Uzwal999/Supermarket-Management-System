    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
     // ADD LOGIC with Validation
        if (jTextField6.getText().isEmpty() || jTextField7.getText().isEmpty() || jTextField5.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all fields!");
        } else {
            Object[] newRow = { jTextField6.getText(), jTextField7.getText(), jTextField5.getText(), jComboBox2.getSelectedItem().toString() };
            sellerList.add(newRow);
            refreshTable();
            jButton8ActionPerformed(null); // Auto-clear
        }   
        
    }//GEN-LAST:event_jButton9ActionPerformed

