    private void btnAddAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddAdminActionPerformed
// 1. Get data from your newly renamed fields
    String name = txtName.getText().trim(); 
    String pass = txtPassword.getText().trim();

    // 2. Validation: Ensure no empty fields (Requirement 5)
    if (name.isEmpty() || pass.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Admin ID and Password are required!");
        return;
    }

    // 3. Logic: Add the new Admin to the SHARED list in Login.java
    // This is the "Create" part of your CRUD requirements
    View.Login.adminList.add(new Model.Admin(name, pass));

    // 4. Success Message
    JOptionPane.showMessageDialog(this, "Admin " + name + " added successfully!");
    
    // 5. Clear fields for next entry
    txtName.setText("");
    txtPassword.setText("");// TODO add your handling code here:
    }//GEN-LAST:event_btnAddAdminActionPerformed

